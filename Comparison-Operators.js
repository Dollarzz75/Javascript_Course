//Write a comparison that checks if a variable is greater than another.
let a = 10;
let b = 5;

// Check if 'a' is greater than 'b'
if (a > b) {
    console.log(`${a} is greater than ${b}`); // Output: 10 is greater than 5
} else {
    console.log(`${a} is not greater than ${b}`);
}

//Check if two variables are less than or equal to each other.
let x = 8;
let y = 10;

// Check if 'x' is less than or equal to 'y'
if (x <= y) {
    console.log(`${x} is less than or equal to ${y}`); // Output: 8 is less than or equal to 10
} else {
    console.log(`${x} is greater than ${y}`);
}

